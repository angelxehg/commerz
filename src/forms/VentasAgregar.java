    private void BtnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAgregarActionPerformed
        // Solo guarda si todos los campos han sido rellenados
        if (CamposRellenos()) {
            // Validaci√≥n de campos
            int ClienteID = (int) SpiCliente.getValue();
            int ProductoID = (int) SpiProducto.getValue();
            int Cantidad = (int) SpiCantidad.getValue();
            float Subtotal = (float) SpiSubtotal.getValue();
            float IVA = (float) SpiIVA.getValue();
            float Total = (float) SpiTotal.getValue();
            // Insertar
            if (Ventas.Agregar(ClienteID, ProductoID, Cantidad, Subtotal, IVA, Total, "Pendiente"))
            {
                JOptionPane.showMessageDialog(rootPane, "Se agreg√≥ la nueva venta");
                this.dispose();
            }
        }
    }//GEN-LAST:event_BtnAgregarActionPerformed

