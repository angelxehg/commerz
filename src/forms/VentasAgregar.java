    public void RecargarPrecio() {
        Consola.Msg("VentasAgregar", "Cargando precio");
        // Buscar precio en base de datos
        int producto = (int) SpiProducto.getValue();
        List<Object[]> R = Productos.BuscarID(producto);
        if (R != null) {
            for (Object[] FilaActual : R) {
                PrecioU = (float) FilaActual[2];
            }
        } else {
            PrecioU = (float) 9.99;
            JOptionPane.showMessageDialog(null, "No se encontr√≥ el producto y/o su precio");
        }
        RecargarValores();
    }
    public void RecargarValores(){
        Consola.Msg("VentasAgregar", "Cargando valores");
        // Cargar valores
        int cantidad = (int) SpiCantidad.getValue();
        float subtotal;
        float iva = (float) SpiIVA.getValue();
        float total;
        // Recargar subtotal en base a la cantidad
        subtotal = PrecioU * cantidad;
        // Recargar monto total respecto al IVA
        total = subtotal * (1 + (iva/100));
        // Cambiar valores
        SpiCantidad.setValue(cantidad);
        SpiSubtotal.setValue(subtotal);
        SpiIVA.setValue(iva);
        SpiTotal.setValue(total);
    }
    
    private boolean CamposRellenos(){
        String Msg = "";
        boolean R = true;
        // Verificar que los campos han sido rellenados
        // Enviar mensaje en caso de error
        if (!R) {
            JOptionPane.showMessageDialog(rootPane, Msg);
        }
        return R;
    }

